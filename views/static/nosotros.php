<h1>Nosotros</h1>
<h2>Petit Pet Shop en el mercado</h2>
<p>Nuestra empresa Petit Shop comenzó en el año 2017 en el local actual, de la calle Bulnes 984, en el barrio de Almagro.</p>
<p>Comenzamos siendo un negocio de barrio que comenzó a crecer gracias al boca a boca de nuestros queridos clientes y vecinos
    que nos ayudaron a que nuestro negocio pudiera prosperar, dentro de un mercado cada vez mas competitivo.</p>
<p>Gracias a ellos es que hoy podemos ofrecer el servicio estupendo que nos caracteriza y tener una red de contactos que nos
    permite ofrecer los mejores precios y las mejores ofertas para todos ustedes.</p>
<p>También es gracias a nuestros empleados que ponen el hombro cada día para ofrecerles la mejor atención posible y
    ofreciéndoles los productos que más convengan a los clientes según su situacion particular, es decir, brindándoles
    una atención personalizada.</p>
<h2>Sobre nuestra misión</h2>
<p>En Petit Pet Shop sabemos lo importante que es tu mascota para vos.
    Vive, duerme, juega y camina a tu lado. Tu mejor compañía te acepta tal cual sos y, a cambio,
    te ofrece todo su amor e incondicionalidad. Tu única responsabilidad es cuidarla y quererla.</p>
<p>Porque amamos a los animales tanto como vos, ponemos a tu alcance todo lo que tu fiel amigo necesita,
    haciéndote ahorrar tiempo para que compartas con él lo que más le gusta: tu compañía.</p>
<p>Desde nuestra página de Petit entregamos cada semana cientos de productos a hogares con perros y/o gatos.
    Llevamos hasta tu puerta todo para tu mascota: alimentos balanceados para perros y gatos, accesorios, golosinas,
    juguetes y mucho más, siendo el pet shop online más completo del país.</p>
